a = 10
b = 3

"""산술 연산자: +, -, *, /, //(몫), %(나머지), **(거듭제곱)"""
print(a + b)  # 13
print(a - b)  # 7
print(a * b)  # 30
print(a / b)  # 3.333...
print(a // b)  # 3
print(a % b)  # 1
print(a ** b)  # 1000

"""비교 연산자: ==, !=, >, <, >=, <="""
print(a == b)  # False
print(a != b)  # True
print(a > b)  # True
print(a < b)  # False

"""논리 연산자: and, or, not"""
print(a > b and b > 1)  # True
print(a > b or b > 5)  # True
print(not (a > b))  # False
